                <h3>Implementation</h3>
                <p>KS3 aims to offer an introduction to a selection of faith traditions, both religious and non-religious. It also aims to enable students to begin to develop their sense of personal identity and their sense of meaning and purpose in the world. Units of work follow the 'Cycle of Enquiry' process as set out in Living Difference IV.</p>

                <p>KS4 follows the AQA (A) specification, and all students are entered for the exam. The religions studied are Buddhism and Christianity. The teaching of the GCSE aims to give an in-depth awareness of the beliefs and practices of these two faith traditions alongside an analytical and evaluative exploration of four themes chosen for their importance to students' lives and general awareness of the societies and communities we are a part of.</p>

                <p><strong>The Cycle of Enquiry:</strong> The KS3 curriculum follows a three-year course taught using the 'Cycle of Enquiry'. Learning is driven by conceptual analysis and so each worldview explored will be taught around a selection of important concepts within this tradition. KS4 follows this pedagogy where appropriate in line with the content required for examination.</p>

