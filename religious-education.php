                <p>Students are encouraged to reflect on a range of individual, school, local, national and world issues in order to develop a vision for their future and the future of others. It is our hope that our students recognise the importance of their own creativity in improving the quality of life not only for themselves but also for future generations.</p>

                <p>Additionally, RE allows the development of rigorous, critical and analytical thinking and argumentation in the evaluation of particular concepts and ways of experiencing the world. Therefore, we aim to foster students' ability to reason, to avoid making assumptions or taking information at face value.</p>

                <p>The school provides Religious Education for all students in accordance with the agreed Hampshire syllabus called '<a href="https://documents.hants.gov.uk/childrens-services/HIAS/living-difference-IV-2021.pdf" target="_blank">Living Difference IV</a>'. This gives students the opportunity to explore a range of religious traditions, cultures and worldviews alongside developing their own in response to these. While a variety of perspectives will be investigated including secular traditions such as Humanism, the curriculum is mainly Christian based. This is to reflect the national and local character of religious belief, and to acknowledge the role that this religious tradition has had in underpinning the development of British values, laws and attitudes.</p>

                <p>The way in which such traditions are explored acknowledges that what it means to be religious can be answered in a number of qualitatively different ways. These include subscription to a set of propositional beliefs, to follow certain practices and traditions, and to have a particular way of existing or being in the world. It recognises that being religious, or non-religious, is not one thing, and that questions of faith and belief are complex.</p>

