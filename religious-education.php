            <h3>Impact</h3>
                <p>Students leave us with a broader cultural capital, a deeper understanding and appreciation of the world around them and the ability to question others and develop their own informed points of view.  They leave us having more deeply explored their own personal sense of meaning, purpose and place within society and the wider world.  They leave as more critical, creative and reflective thinkers, able to articulate their vision for the society in which they live.  Students will leave with a GCSE demonstrating their achievement and progress in this, and more prepared for life in modern Britain.</p>

