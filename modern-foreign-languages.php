            <h3>Purpose</h3>
            <p>Why should you learn a foreign language?  Well -  <em>60% of British employers look for candidates who can speak a foreign language as well as English</em>.</p>
            <p>Whether working on the shop floor dealing with foreign customers, in the port with travellers from Europe, in the transport industry with goods and people from abroad, in the tourist industry travelling or needing to check facilities overseas for your clients, in the building industry sourcing materials or seeking contracts from around the world, in the legal system or in banking, the likelihood is that someone who can make themselves understood and can understand a second language will be more useful than someone who can only speak English.</p>
            <p>We teach Spanish because many of our families holiday in Spain and your child will not have a choice in which language he/she learns. Learning a language will teach your child long-life skills – they will learn to listen carefully to not only the words but the intonation, develop the confidence to have a go even if they are unsure, communicate with others and understand others’ cultures and views. Students will be trained to notice patterns in the Spanish language and in English which will lead them to value the need for accuracy, and appreciate cultural differences.</p>
            <p>Learning a foreign language also reinforces basic literacy skills in English. These are all skills which employers actively look for.</p>

            <h3>Intent</h3>
            <ul class="cirle">
                <li>To provide all students with an understanding of the Spanish language and culture.</li>
                <li>To enable opportunities for all pupils at home and abroad.</li>
                <li>To encourage positive attitudes to learning foreign languages and open our students’ minds to another way of thinking, speaking and living.​</li>
                <li>To develop the ability to use language effectively for communication.</li>
                <li>To offer insights into the culture and civilisation of other countries combating xenophobia and broadening horizons.​</li>
                <li>To develop pupils’ understanding of themselves, their own language and culture.​</li>
                <li>To promote learning skills pupils will be able to use in other areas of their studies – analysis, logic, memory, reading skills…</li>
                <li>To support literacy objectives in English.</li>
                <li>Develop pupils who: 
                    <ul>
                        <li>become more confident, showing a greater willingness to persevere.</li>
                        <li>work independently towards a self-determined goal.</li>
                        <li>are excited by the opportunity of continuing with a foreign language  into KS4 for a recognised, accredited qualification and who strive to achieve higher results.</li>
                    </ul>
                </li>
            </ul>
            
