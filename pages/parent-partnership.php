                <p class="sub-section-heading"><strong>Academic Progress</strong></p>
                <ol>
                    <li>All parents will receive a data-based report on their child's progress 4 times per year.</li>
                    <li>All parents will receive a clearly written full report on their child's progress once per year. The report will explain simply and clearly what has been studied, how the student has made progress and what precisely they need to do to improve.</li>
                    <li>All parents to be actively encouraged to attend a Parents Evening to discuss their child's progress with subject teachers once per year. In the case of Year 8 this will be part of the options process.</li>
                    <li>All parents actively encouraged to attend Student Review Meetings to discuss their child's progress and academic targets with mentors twice per year.</li>
                    <li>All parents in Year 11 invited to attend additional parents' evenings aimed at helping them to better support their child's aspirations and examination success.</li>
                </ol>

                <p class="sub-section-heading"><strong>Interventions</strong></p>
