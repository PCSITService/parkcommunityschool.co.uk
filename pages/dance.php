                <h3>Intent</h3>
                <p><em>"Creativity takes courage." – Henri Matisse</em></p>
                <p>The intention of Performing Arts at Park is to spark curiosity and foster a deep passion for creativity within the hearts and minds of our students. We strive to develop key life skills while nurturing imaginative, expressive, and innovative individuals, preparing them for further education and future careers.</p>

                <p>Our curriculum supports students in becoming independent and inventive through a diverse range of schemes of work that encourage exploration of different styles and practitioners. Furthermore, Performing Arts promotes a strong sense of self-worth and supports the growth of confidence, resilience, and autonomy.</p>

                <p>The skills developed through studying Dance are invaluable in later life. For example, performing with confidence in front of an audience, demonstrating physical control and discipline, and collaborating effectively as part of a group are all highly transferable skills. The creativity, commitment, and resilience required to rehearse and refine choreography are beneficial in many careers. Dance can provide a strong foundation not only for a career in performance, choreography, or teaching, but also in fields such as the creative industries, fitness, therapy, and events management, among many others.</p>
                        
                <h3>Implementation</h3>
                <p><strong>Releasing Potential</strong> – The KS3 curriculum enables students to build up a repertoire of basic skills that students need to master before they go into KS4. Students learn a range of different choreographic devices and performance skills that build their self-confidence and teamwork skills that are fundamental to their learning. Students have the opportunity to learn different styles of Dance and are encouraged to self-assess and evaluate the work of others. Examples of units are Video games, Mission impossible, Swan Song, Soldiers and Performing arts project. In year 7 the focus is on creativity and exploration which then leads them onto practitioners and exploring different styles in year 8. All units will equip students with relevant skills in order to access our KS4 courses. Students will also have the opportunity to achieve an Arts Award within their theory lessons.</p>
                
                <p>Homework is set every week via Multiple Choice Questioning (MCQ) and homework booklet, these alternate each week. KS3 students have the opportunity to be involved in our Performing Arts ambassador team. This involves working with students across our feeder schools as well as supporting our department with shows and extra curricular activities.</p>

                <p>Once in KS4 students then begin studying for the BTEC TECH Award Level 2 in Performing Arts. The course has three components and learners are required to achieve all three in order to pass the course.</p>

                <div class="component-box">
                    <p><strong>Component 1 – 'Exploring the Performing Arts'</strong></p>
                    <p>This unit is an internal assessment set by Pearson, marked by the centre and moderated by Pearson. Learners will develop their understanding of the Performing Arts by examining the work of performing arts professionals and the processes used to create performance.</p>
                    <p>Learning outcome A – Investigate how professional performance or production work is created.</p>
                    <p>Learning outcome B – Demonstrate understanding of the skills, techniques, and approaches used by professionals to create performance/production work.</p>
                </div>

                <div class="component-box">
                    <p><strong>Component 2 – 'Developing skills and techniques in the Performing Arts'</strong></p>
                    <p>This is an internal assessment set by Pearson, marked by the centre and moderated by Pearson. Learners will develop their performing arts skills and techniques through the reproduction of acting, dance and musical theatre repertoire as performers or designers.</p>
                    <p>Learning outcome A – Use rehearsal or production/design processes</p>
                    <p>Learning outcome B – Apply skills and techniques in Performance or realisation</p>
                    <p>Learning outcome C – Review own development and application of performance or design skills</p>
                </div>

                <div class="component-box">
                    <p><strong>Component 3 – 'Responding to a brief'</strong></p>
                    <p>This unit is an externally marked unit set by Pearson. Learners will be given the opportunity to work as part of a group to contribute to a workshop performance as either a performer or a designer in response to a brief and stimulus.</p>
                </div>

                <p>Homework is set every 2 weeks via Multiple Choice Questioning (MCQ).</p>

