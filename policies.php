    <script>
        document.addEventListener('DOMContentLoaded', function() {
            // Search functionality
            const searchInput = document.getElementById('policy-search');
            const policyItems = document.querySelectorAll('.policy-item');
            
            // Sort policies alphabetically within each section
            const statutoryGrid = document.getElementById('statutory-policies-grid');
            const nonStatutoryGrid = document.getElementById('non-statutory-policies-grid');
            
            // Function to sort policies alphabetically
            function sortPolicies(container) {
                const items = Array.from(container.children);
                items.sort((a, b) => {
                    const titleA = a.querySelector('.policy-title').textContent.trim().toLowerCase();
                    const titleB = b.querySelector('.policy-title').textContent.trim().toLowerCase();
                    return titleA.localeCompare(titleB);
                });
                
                // Remove existing items
                while (container.firstChild) {
                    container.removeChild(container.firstChild);
                }
                
                // Add sorted items back
                items.forEach(item => container.appendChild(item));
            }
            
            // Sort the policies
            sortPolicies(statutoryGrid);
            sortPolicies(nonStatutoryGrid);
            
            // Search functionality
            searchInput.addEventListener('input', function() {
                const searchTerm = this.value.toLowerCase().trim();
                
                // Show all policies if search is empty
                if (searchTerm === '') {
                    policyItems.forEach(item => {
                        item.style.display = 'flex';
                    });
                    
                    // Show all section headings
                    document.querySelectorAll('.section-heading').forEach(heading => {
                        heading.style.display = 'block';
                    });
                    
                    return;
                }
                
                // Filter policies based on search term
                policyItems.forEach(item => {
                    const policyName = item.getAttribute('data-policy-name').toLowerCase();
                    const policyTitle = item.querySelector('.policy-title').textContent.toLowerCase();
                    
                    if (policyName.includes(searchTerm) || policyTitle.includes(searchTerm)) {
                        item.style.display = 'flex';
                    } else {
                        item.style.display = 'none';
                    }
                });
                
                // Check if any policies are visible in each section
                const sections = document.querySelectorAll('.policy-section');
                sections.forEach(section => {
                    const visibleItems = section.querySelectorAll('.policy-item[style="display: flex;"]');
                    const sectionHeading = section.querySelector('.section-heading');
                    
                    if (visibleItems.length === 0) {
                        sectionHeading.style.display = 'none';
                    } else {
                        sectionHeading.style.display = 'block';
                    }
                });
            });
            
            // Add count of policies to section headings
            const updatePolicyCounts = () => {
                const statutoryCount = statutoryGrid.children.length;
                const nonStatutoryCount = nonStatutoryGrid.children.length;
                
                const statutoryHeading = document.querySelector('#statutory-policies .section-heading');
                const nonStatutoryHeading = document.querySelector('#non-statutory-policies .section-heading');
                
                statutoryHeading.setAttribute('data-count', `(${statutoryCount})`);
                nonStatutoryHeading.setAttribute('data-count', `(${nonStatutoryCount})`);
            };
            
            updatePolicyCounts();
        });
    </script>

