           <h3>Intent</h3>
            <p>Art, craft and design embody some of the highest forms of human creativity. At Park we believe that high quality art and design should engage, inspire and challenge pupils; equipping them with the knowledge and skills to experiment, invent and create their own works of art, craft and design. As pupils progress, they will be able to think critically and develop an understanding of art and design.  All pupils will have the opportunity to produce creative work by exploring their ideas and recording their experiences in a variety of ways using a range of materials, processes and techniques. Pupils will have the opportunity become proficient in drawing, painting, clay and other art, craft and design techniques whilst also developing their use of art vocabulary when evaluating and analysing creative works. Pupils will also develop their subject knowledge and understanding through the identification of key artists, craft makers and designers that have been key in the historical and cultural development of the subject.</p>

            <h3>Implementation</h3>
            <p>At KS3 pupils are taught to develop their creativity and ideas through a critical understanding of artists and designers, which combined with mastery of key technical skills can then be used to inform their own work. Pupil projects in Year 7 are focused art appreciation and engagement, through units looking at portraits, Mexican Day of the Dead and 3d art through developing movie props for a YA novel adaptation. These are further developed in Year 8 through studies of Greyson Perry, mixed media manipulation and Scarpace. KS3-4 curriculum projects are designed to ensure that all pupils have experience of the history of art, craft, design including periods, styles and major movements and direct links are made throughout the duration of the project work. Throughout all key stages’ pupils are given the opportunity to use a range of techniques to record their observations in sketchbooks as a basis for exploring their ideas. Pupils are expected to experiment with a varied range of media, which can include painting, drawing, mixed media techniques, collage, clay, photography, digital manipulation packages and textiles. Pupils will revisit media techniques and processes many times to allow for mastery of the skill and will continually analyse and evaluate their own work and the work of others in order to understand how to adapt and refine their own work to ensure the most competent visual image is presented, or the best application for their work has been selected. The GCSE assessment structure is embedded within the curriculum to ensure students opting for KS4 art are well versed in following a basic project structure which further supports the externally set exam completed at the end of the GCSE Course.</p>
            <ul class="circle">
                <li>AO1 - Pupils will learn how to research and evaluate a range of historical and contemporary artists, evaluate their work and show clear understanding of visual language. They will then respond to it with a pastiche to show understanding.</li>
                <li>AO2 - Pupils will learn how to refine their work through purposeful experimentation with a varied range of materials and techniques presented in individual sketchbooks and boards . Annotation and explanation are essential and subject specific vocabulary is used.</li>
                <li>AO3 - Pupils will develop their ability to skillfully record observations through drawing, annotations and other appropriate media. This may include; Mind maps, Design sheets, Working drawings, Thumbnail sketches, Annotations and Photographic contact sheets.</li>
                <li>AO4 - Pupils will learn how to create a fully developed ‘final piece’ which shows clear understanding of visual language and is clearly influenced by the artists they have researched either as a class or as individuals.</li> 

            </ul>
            <p>Due to the nature of the course we are able to give pupils a degree of ownership over how the course is organised for them. While the first unit is always prescribed, subsequent units are chosen by classes after suitable choices are presented to them. In addition to this, during the course of a unit pupils will study on average 3 artists together as a class before finding their own influences, ensuring that outcomes will be individual and personal. This same structure is used for the exam unit, when there could be as many as 7 different topics/starting points being investigated within one class.
            </p>

             
            <h3>Impact</h3>
            
            <ul class="circle">
                <li>Pupils have a competent handle on art as a visual language and be able to apply this language in a purposeful way.</li>
                <li>Pupils have a love of art, with an appreciation for the work of others evidenced by the way they create their own work and how they articulate their thoughts using appropriate subject specific language. ( Orally and written)</li>
                <li>Pupils leave with a broader cultural capital, a deeper understanding and appreciation for the world around them.</li>
                <li>Pupils are able to tackle creative problems using the creative process to produce a successful solution.</li>
                <li>Pupils are able to work independently, organise themselves, use equipment confidently and problem solve to achieve  positive results.</li>
                <li>Pupils are able to critique their work, the work of others confidently and work independently to improve and extend work based on feedback and guidance notes.</li>
            </ul>

