            <h2>Main Theatre</h2>
            <p>Our main theatre comprises of a 245-seat auditorium with raked seating looking down onto a flat “black box” stage. The stage has greater wing space than many local professional venues and a crossover. It is equipped with adequate masking to hide all backstage areas and in addition to the up-stage black tabs there is also a white cyclorama.</p>
            <p>Usable stage area is approximately 9m wide by 10m deep which includes a 2.5m apron in front of the FOH tabs. Overhead height is in excess of 6m.</p>
            <p>The theatre features retractable seating which allows for the space to be set up in a cabaret style with chairs and tables. In this configuration the venue can hold 100 people seated and 80 standing and have the bar located in the auditorium; an ideal set up for concerts and cabaret events.</p>
            <p>This space is perfect for dance shows, musical theatre, theatre productions, concert venue, rehearsal room, <a href="/community-services.php">party room, wedding receptions and for Children’s parties</a>.</p>
