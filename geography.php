                <h3>Vision</h3>
                <p>As the future generation responsible for our planet, we want our students to have a sense of respect for the world and the people in it, enhanced by a sound knowledge and understanding of both physical and human geography. For our students to develop an analytical and evaluative understanding of the world through discussion and written responses on case studies and topical issues.</p>
                
                <h3>Intent</h3>
                <p>Geography at Park Community School is designed to develop our students' curiosity about the physical and human world around them. Our students will investigate the local area as well as a variety of case studies from around the world. As a department we are keen to ensure that students understand how geography influences their everyday lives and how geography shapes the world around them. We also aspire to produce global thinkers; students are encouraged to think about how we are interdependent with the world around us.</p>
                
                <p>Our initial Year 7 curriculum focuses on the geography of our local area and how this links to our identity and heritage, igniting a sense of pride in our community. Year 7 also covers some foundations in knowledge which are then drawn upon throughout the Key Stage 3 geography journey. In Years 8 and 9 we broaden students' knowledge of human and physical geography globally, through a range of topics and specific case studies.</p>
                
                <p>Within Key Stage 3 we aim to develop geographical skills, including the ability to use a range of maps, collect and analyse data and the ability to communicate information in a variety of ways. We also aim to provide memorable fieldwork experiences allowing all year groups to see geography in action and investigate it themselves.</p>

                <h3>Implementation</h3>
                <p>We have a progressive geography curriculum which builds on prior knowledge and skills each year. Learners will revisit geographical skills and knowledge to embed and deepen understanding. We have a clear structure for our geography lessons:</p>
                
                <p>All lessons begin with a 'do now' task focusing students on entry, which is a retrieval quiz enabling students to strengthen their memories by recalling information from the previous lessons and topics. The student is then ready to investigate new information from the lesson. The new information is presented in a range of ways, often through collaborative enquiry and teaching and learning games. Students then get an opportunity to recall, discuss and share new information before being asked to apply their new knowledge either as a chain of reason or in an exam style question.</p>
                
                <p>The geography department is ambitious for our students; teaching to the top, scaffolding so all can achieve. Reading is intrinsically linked into geography lessons with guided reading tasks in every topic linked to geographical texts. In geography we operate a 'no hands up' policy to hold the students' focus, ensuring they are ready to be called upon.</p>
                
                <p>In Key Stage 3 geography we use multiple choice question testing for homework where students are asked to recall knowledge from the previous lessons. The multiple choice questions are levelled to allow students to assess their progress and allow teachers to address any misconceptions. There are also termly geography assessments focusing on geographical skills. Students complete a self/peer and have a teacher assessed piece every half term. This is tracked on our geography matrix to allow students to see how they can progress.</p>
                
                <p>Each of the topics in Key Stage 3 has competencies linked to the age-related expectations. This allows students to track their progress and achieve age-related expectation in more manageable steps. To assist students in finding what they love, we have half-termly 'For the Love of' tasks, igniting the passion for the topic being studied in a range of ways. Students can find all their lessons and more on their geography student portal. This ensures students can learn from home should the situation arise or simply use it as a revision resource.</p>

                <p>At GCSE we follow the Eduqas B GCSE specification which can be found online <a target="_blank" href="https://www.eduqas.co.uk/qualifications/geography-gcse-b/#tab_keydocuments">here</a>.</p>

