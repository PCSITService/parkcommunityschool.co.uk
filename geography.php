            <h3>Intent</h3>
            <p>As the future generation responsible for our planet, we want our students to respect our world and the people in it, building a sound knowledge and understanding of both physical and human geography. They will do this by developing an analytical and evaluative understanding of the world through discussion and written responses on case studies and topical issues.</p>
            <p>Geography at Park Community School is designed to develop our studentsâ€™ curiosity about the physical and human world around them. Our students will investigate the local area as well as a variety of case studies from around the world. As a department, we are keen to ensure that students understand how geography influences their everyday lives and how they impact the future of our planet. We also aspire to produce geographical enquirers, asking questions and developing an understanding about different cultures and places around the world.</p>
            

            <h3>Implementation</h3>
            <p>Our year 7 curriculum is focusing on the geography of our local area and how this links to our identity and heritage, igniting a sense of pride in our community. In years 8 and 9, we broaden students' knowledge of human and physical geography locally and globally through a range of topics and specific case studies. Within Key Stage 3, we aim to develop geographical skills, including the ability to use a range of maps, collect and analyse data and the ability to communicate information in a variety of ways. We also aim to provide memorable fieldwork experiences allowing all year groups to see geography in action and investigate it themselves.</p>
            <p>We have a progressive geography curriculum which builds on prior knowledge and skills each year. Learners will revisit geographical skills and knowledge in order to embed and deepen understanding and build towards mastery.</p>
            <p>The content of the lessons uses case studies from around the world but also looks to make connections to local geography, so that the students can recognise the responsibility and impact they can have.</p>
            <p>At GCSE we follow the AQA GCSE specification which can be found online <a target="_blank" href="https://filestore.aqa.org.uk/resources/geography/specifications/AQA-8035-SP-2016.PDF">here</a></p>

            <h3>Impact</h3>
            <p>The impact of our geography curriculum is to equip our students with lifelong geographical skills and knowledge. As the future generation responsible for our planet, we want to instil a sense of responsibility and respect for the world, which is rooted in their knowledge of sustainable living.</p>
            <p>Students who take geography to GCSE will achieve a qualification that demonstrates their achievement and progress. Further to this, they will leave with the subject specific knowledge and transferrable skills which will allow them to continue their learning beyond school.</p>

