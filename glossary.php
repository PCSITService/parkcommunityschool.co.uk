                    <h2 style="color: #5fbcea;" id="letterA">A</h2>

                    <h4>Academies</h4>

                    <p>Academies are independent, publicly-funded schools. Academies receive their funding directly from the Department for Education, rather than the local council or educational authority. Academies must follow the same rules on admissions, special educational needs and exclusions as other state schools, but they do not have to follow the national curriculum. Each academy or academy chain is run by a trust which employs the staff.</p>

                    <h4>A Level</h4>

                    <p>Short for General Certificate of Education Advanced Level, A Levels are studied between the ages of 16-18 years. Exams are often sat at the end of both years. The first year of A Level is called AS Level (Advanced Subsidiary) and the second year is called A2. Together they make up an A Level. The curriculum is set by the government but the exams and specifications (syllabuses) are set by individual exam boards.</p>
                    
                    <h4>Apprenticeships</h4>

                    <p>A way of learning and achieving qualifications whilst in employment gaining workplace experience.</p>
                    
                    <h4>AQA</h4>

                    <p>Assessment and Qualifications Alliance – an awarding body.</p>
                    
                    <h4>Attainment 8</h4>

                    <p>Used in secondary schools, Attainment 8 records a pupil's achievement across eight qualifications, including Maths and English.</p>


                    <h2 style="color: #5fbcea;" id="letterB">B</h2>

                    <h4>BA (Hons)</h4>

                    <p>Bachelor of Arts (with Honours) – a degree award.</p>
                    
                    <h4>BSc (Hons)</h4>

                    <p>Bachelor of Science (with Honours) – a degree award.</p>
                    
                    <h4>BTEC</h4>

                    <p>Business and Technician Education Council – offering vocational qualifications for access to specific careers or higher education.</p>


                    <h2 style="color: #5fbcea;" id="letterC">C</h2>

                    <h4>Comprehensive school</h4>

                    <p>Comprehensive schools are secondary level state-funded schools, that do not select their students on the basis of achievement or their academic aptitude. They should accept any applicable students from within a geographical area. They differ from grammar schools, which selects pupils on the basis of their grade at the 11+ exam. Today, most comprehensives are also academies.</p>


                    <h2 style="color: #5fbcea;" id="letterD">D</h2>

                    <h4>DfE</h4>

                    <p>Department for Education – a department of the UK government responsible for issues affecting young people in England up to the age of 19.</p>
                    
                    <h4>DIPHE</h4>

                    <p>Diploma of Higher Education.</p>


                    <h2 style="color: #5fbcea;" id="letterE">E</h2>

                    <h4>Edexcel</h4>

                    <p>Edexcel is a UK exam board offering a range of qualifications including GCSE.</p>
                    
                    <h4>ECM</h4>

                    <p>Every Child Matters – a government initiative that aims to ensure that every child, whatever their background or their circumstances, has the support they need to be healthy, stay safe, enjoy and achieve, make a positive contribution and achieve economic well-being.</p>


                    <h2 style="color: #5fbcea;" id="letterF">F</h2>
                    
                    <h4>FE</h4>

                    <p>Further education – post-compulsory.</p>
                    
                    <h4>Framework</h4>

                    <p>A guide developed for Key Stage 2 and 3 outlining how and when subjects should be taught.</p>
                    
                    <h4>Free schools</h4>

                    <p>A free school is a variety of academy; a state-funded, free-to-attend, independent school which is not controlled by a local educational authority. A free school is understood to be "free" from local authority control, rather than describing free for students to attend. Like other academies, free schools may have sponsors, but the sponsor may not control more than 1/5 of a free school's board of trustees. Free schools are allowed to employ teachers who have not gained Qualified Teacher Status (QTS).</p>


                    <h2 style="color: #5fbcea;" id="letterG">G</h2>

                    <h4>GCSE</h4>

                    <p>Short for General Certificate of Secondary Education, GCSEs are studied between the ages of 14-16 years and are assessed by formal exams. GCSEs cover a wide range of subjects and each student chooses several subjects to study. Most are expected to take English, Mathematics, and Science. Coursework may count for part of the final mark depending on the subject and syllabus. The curriculum is set by the government but exams and specifications (syllabuses) are sent by individual exam boards.</p>

                    <h4>Gifted and Talented</h4>

                    <p>Gifted and Talented is a phrase used to describe high ability children. The gifted are those with high ability in one or more academic subject, and the talented are those with high ability in sport, music, visual arts and/or performing arts.</p>

                    <h4>Grammar schools</h4>

                    <p>Grammar schools are schools that select their students on the basis of their academic aptitude. Historically, grammar schools were the selective level of the Tripartite System of secondary education in the UK from the 1940s to the 1960s. With the introduction of comprehensive schools in the 1970s, many grammar schools converted to comprehensives or became independent, fee-paying schools. Some areas of England, such as Kent and Lincolnshire, retain most elements of the Tripartite System, and select students for grammar schools on the basis of their results at the 11+ exam.</p>

                    <h4>Guided Reading</h4>

                    <p>Guided Reading bridges the gap between Shared and Independent Reading. Children are grouped by ability, with up to six children in a group. A book is chosen at 'instructional level' so that children will only have difficulty with one word in ten. The teacher leads the session, reinforcing various reading strategies and focusing on individual children as they read.</p>


                    <h2 style="color: #5fbcea;" id="letterH">H</h2>

                    <h4>HE</h4>

                    <p>Higher education – at university level.</p>


                    <h2 style="color: #5fbcea;" id="letterI">I</h2>

                    <h4>IB</h4>

                    <p>International Baccalaureate – an internationally recognised qualification offered at three levels.</p>

                    <h4>ICT</h4>

                    <p>Information and communication technology.</p>


                    <h2 style="color: #5fbcea;" id="letterJ">J</h2>

                    <h4>JCQ</h4>

                    <p>Joint Council for Qualifications.</p>


                    <h2 style="color: #5fbcea;" id="letterK">K</h2>

                    <h4>Key Stage</h4>

                    <p>The national curriculum in the UK defines the minimum educational requirements for students of compulsory school age (5-16 years). It is organised on the basis of five Key Stages. At the end of each Key Stage, students take assessments to determine the levels they have reached.</p>

                    <ul>
                        <li>Foundation (Reception/3-5 year olds) - Infant</li>
                        <li>Key Stage 1 (Years 1-2/5-7 year olds) - Nursery</li>
                        <li>Key Stage 2 (Years 3-6/7-11 year olds) - Primary</li>
                        <li>Key Stage 3 (Years 7-9/11-14 year olds) - Lower Secondary</li>
                        <li>Key Stage 4/GCSE (Years 10-11) - Upper Secondary</li>
                    </ul>

                    <p>After GCSEs students can opt to enter Key Stage 5 and take A Levels (Years 12-13/16-18 years old).</p>


                    <h2 style="color: #5fbcea;" id="letterL">L</h2>

                    <h4>LA</h4>

                    <p>Local authority – a council that has responsibility for providing education to pupils of school age and adult education in its area.</p>


                    <h2 style="color: #5fbcea;" id="letterM">M</h2>

                    <h4>Multi-academy trust (MAT)</h4>

                    <p>A multi-academy trust (MAT) is a a group of academies run jointly by one board of directors, working together as one entity to pool resources and improve standards across the trust. A multi-academy trust is usually headed by a CEO, who differs from a traditional headteacher, and may not be a qualified teacher themselves.</p>


                    <h2 style="color: #5fbcea;" id="letterN">N</h2>

                    <h4>National Curriculum, The</h4>

                    <p>The national curriculum is set by the government in the UK and is a program of study outlining what must be taught at each level and giving attainment targets. Not all schools have to follow the national curriculum (such as academies and private schools).</p>

                    <h4>NVQ</h4>

                    <p>National Vocational Qualification – workbased awards achieved through assessment and training.</p>

                    <h4>NQT</h4>

                    <p>Newly qualified teacher.</p>


                    <h2 style="color: #5fbcea;" id="letterO">O</h2>

                    <h4>OCR</h4>

                    <p>OCR (Oxford Cambridge and RSA Examinations) is a UK exam board offering a range of qualifications including GCSE and A Level.</p>

                    <h4>Ofqual</h4>

                    <p>Office of the Qualifications and Examination Regulator – regulatory body for all awarding bodies.</p>

                    <h4>Ofsted</h4>

                    <p>Office for Standards in Education, Children’s Services and Skills – regulator of care for children and young people, and inspector of education and training for learners of all ages.</p>


                    <h2 style="color: #5fbcea;" id="letterP">P</h2>

                    <h4>Private schools</h4>

                    <p>Private schools (also known as independent schools and in some cases public schools) are schools that charge fees in order to attend. They do not get funding from the government, and do not have to follow the national curriculum. About half of private schools are inspected by Ofsted, while the other half are inspected by either the Independent Schools Inspectorate or the School Inspection Service. Some older and more expensive private schools are known as "public schools" although they are not part of the public sector; these schools were considered historically public because they were open for anyone to attend, provided they could pay the fees.</p>

                    <h4>Progress 8</h4>

                    <p>Progress 8 is a measurement scale used to determine the progress a pupil makes from the end of primary school to the end of Key Stage 4. It was introduced in 2016 as a new measure for determining overall school performance. Under Progress 8, pupils' results are compared to the progress of other pupils nationally with similar levels of attainment (using Attainment 8). Students are then judged relative to their attainment and how much progress they have made since the end of primary school. The average scores of a cohort makes up a school's Progress 8 score.</p>


                    <h2 style="color: #5fbcea;" id="letterQ">Q</h2>

                    <h4>QCA</h4>

                    <p>Qualifications and Curriculum Authority.</p>

                    <h4>QCF</h4>

                    <p>Qualifications and Credit Framework – framework for recognising skills and qualifications.</p>

                    <h4>Qualification</h4>

                    <p>An award made by an awarding body to demonstrate a learner’s achievement or competence.</p>

                    <h4>QTS</h4>

                    <p>Qualified Teacher Status – achieved through university studies and teaching placement.</p>


                    <h2 style="color: #5fbcea;" id="letterR">R</h2>
                    <h2 style="color: #5fbcea;" id="letterS">S</h2>

                    <h4>SATs</h4>

                    <p>SATs (officially known as National Curriculum assessments) are the national curriculum tests that are taken at the end of Key Stages 1 and 2. They are designed to help teachers measure students' progress, how much of the curriculum they understand and what they might achieve in the future. SATs attainment levels were abolished in 2016 and replaced with a new judgement framework.</p>

                    <h4>SEN (Special Educational Needs)</h4>

                    <p>Children with special educational needs all have learning difficulties or disabilities that make it harder for them to learn or access education than most children of the same age. These children may need extra or different help from that given to other children of the same age.</p>

                    <h4>Special consideration</h4>

                    <p>Post-assessment adjustment to grades reflecting temporary illness, injury or other indisposition at the time the assessment was taken.</p>


                    <h2 style="color: #5fbcea;" id="letterT">T</h2>

                    <h4>Technicals</h4>

                    <p>OCR vocational qualifications at Level 2 and Level 3 for students aged 16+, designed to enable progress to employment or further education within the workplace.</p>

                    <h2 style="color: #5fbcea;" id="letterU">U</h2>

                    <h4>UCAS</h4>

                    <p>University and Colleges Admission Service.</p>

                    <h4>UCI</h4>

                    <p>Unique Candidate Identifier.</p>

                    <h4>ULN</h4>

                    <p>Unique learner number.</p>

                    <h4>UMS</h4>

                    <p>Uniform Mark Scale.</p>


                    <h2 style="color: #5fbcea;" id="letterV">V</h2>
                    <h2 style="color: #5fbcea;" id="letterW">W</h2>

                    <h4>WJEC</h4>

                    <p>WJEC (Welsh Joint Education Committee) is a UK exam board offering a range of qualifications including GCSE.</p>

