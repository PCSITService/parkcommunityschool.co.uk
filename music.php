            <p><strong>Releasing Potential</strong> The Key Stage 3 curriculum at Park is broad and enables students to build up a good repertoire of songs and improve their vocal skills, learn basic keyboard skills and progress to playing with both hands. In years 7, 8, and 9 Genre projects last between 5-7 weeks of exploring, learning, precise practicing, challenging, reviewing, performing, composing, and listening and evaluating activities, where pupils have opportunities to work individually, in pairs and in larger groups on a range of different instruments. They are encouraged to evaluate and improve upon their ideas as well as discussing the work of their peers in a positive, supportive atmosphere which leads towards setting personalised goals of improvement for the future.</p>
