                    <h2>Data Retention</h2>
                    
                    <p>As a Hampshire County Council maintained school, we follow Hampshire County Council's retention schedule for different types of information:</p>
                    
                    <h3>Student Records</h3>
                    <ul>
                        <li><strong>Academic records:</strong> Retained permanently for historical purposes as per Hampshire guidelines</li>
                        <li><strong>Attendance and behavior records:</strong> 7 years after leaving school following Hampshire's retention policy</li>
                        <li><strong>Medical information:</strong> Until 25th birthday or 8 years after leaving school as per Hampshire health protocols</li>
                        <li><strong>Child protection records:</strong> Until 25th birthday following Hampshire safeguarding requirements</li>
                        <li><strong>Special educational needs records:</strong> Until 30th birthday as per Hampshire SEND guidelines</li>
                    </ul>
                    
                    <h3>Staff Records</h3>
                    <ul>
                        <li><strong>Personnel files:</strong> Retained according to Hampshire County Council HR retention schedules</li>
                        <li><strong>Payroll records:</strong> As per Hampshire County Council finance retention requirements</li>
                        <li><strong>Training records:</strong> Following Hampshire's professional development record keeping</li>
                        <li><strong>DBS checks:</strong> Not retained; confirmation of check only, as per Hampshire safeguarding policies</li>
                    </ul>
                    
                    <h3>School-Specific Records</h3>
                    <ul>
                        <li><strong>Website analytics:</strong> 26 months maximum following Hampshire's digital policy</li>
                        <li><strong>Email communications:</strong> 7 years for significant correspondence as per Hampshire guidelines</li>
                        <li><strong>Contact form submissions:</strong> 2 years unless resolved sooner, following Hampshire data minimization principles</li>
                    </ul>
                    
                    <p>All retention periods comply with Hampshire County Council's corporate retention schedule and legal requirements.</p>
                    
