                <p>As part of our commitment to working in partnership with schools, partners and parents, I am sending this information out expeditiously to ensure you are aware of an internet 'suicide-influencing game' called The MOMO Challenge which encourages children to harm themselves and is reported to be linked to several deaths around the world. It is now appearing across the UK.</p>
                
                <p>Below is a brief summary of what the MOMO Challenge is and we ask that you share this information among your colleague and parent networks.</p>
                
                <p>Mirroring the 'Blue Whale' suicide-game of 2017, The MOMO Challenge is targeted at children and young people through social media by people presenting as MOMO, a terrifying looking doll.</p>
                
                <p>The doll encourages them to add a contact on messaging service WhatsApp from an unknown number. Once contact is made, children are subsequently bombarded with terrifying images and messages reportedly ranging from threats and dares which encourage them to self-harm and even commit suicide.</p>
                
                <p>Although known of in other parts of the world since last year, it appears to be making its way across the UK.</p>
                
                <p>MOMO has been associated with multiple platforms used by children including YouTube, KidsTube, Instagram, Facebook and WhatsApp.</p>
                
                <p>Police in Northern Ireland have warned parents after a mother reported finding the 'creepy game' on her 7-year-old daughter's iPad. They have since posted a message about the challenge on Facebook, referring to a video of a MOMO interaction in America showing an 'ominous sounding voice recording' sent to a child, telling them to use a knife on their own throat â€“ with another making threats against a child's family if a 'challenge' is not completed, describing it as 'chilling viewing'.</p>
                
                <p>So far, a 12-year-old girl and a 16-year-old boy, both from Colombia, are thought to have killed themselves after taking part in the MOMO challenge. The deaths occurred in September, just 48 hours apart, and it was thought that the two knew each other. After the police seized their phones, they said they found messages that were linked to the game.</p>
                
                <p>A concerned British mother spoke to the media last week to highlight that the 'sick game' had already spread to Manchester, after her 7-year-old son told her that some of his schoolmates told him to look up the MOMO challenge. She went on to say that when they watched the video, the MOMO character told him to tell everyone to be in fear of MOMO or it will kill him in his sleep, causing distress to the boy and his friends.</p>
                
                <p>It is understood the original artwork used by the hackers has been taken from a designer in Japan who has no connection whatsoever with the MOMO challenge.</p>
                
                <p><strong>The Real Motive?</strong> Police in Northern Ireland outlined in their statement that they believe the game is being used by hackers seeking and harvesting information, warning that while it is perceived as a horrendous 'suicide game' targeting children, likely to get thousands of hits, it 'misses the bigger picture'. There are now numerous variations and imitators.</p>
                
